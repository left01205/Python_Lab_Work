import math
import random

x = 4.3
y = 2.7


sqrt_x = math.sqrt(x)
print(f"The square root of {x} is {sqrt_x}")


ceil_y = math.ceil(y)
floor_y = math.floor(y)
print(f"The ceiling of {y} is {ceil_y}")
print(f"The floor of {y} is {floor_y}")

random_int = random.randint(1, 10)
print(f"Random integer between 1 and 10: {random_int}")
random_float = random.random()
print(f"Random floating-point number between 0 and 1: {random_float}")